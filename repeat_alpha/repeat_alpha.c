#include<unistd.h>

int main(int ac, char** av)
{
    int i;
    char k;

    i = 0;
    if (ac == 2)
    {
        while (av[1][i])
        {
            if (av[1][i] >= 'a' && av[1][i] <= 'z')
            {
                k = 'a';
                while (av[1][i] > k)
                {
                    write(1, &av[1][i], 1);
                    k++;
                }
            }
            else if (av[1][i] >= 'A' && av[1][i] <= 'Z')
            {
                k = 'A';
                while (av[1][i] > k)
                {
                    write(1, &av[1][i], 1);
                    k++;
                }
            }
            write(1, &av[1][i], 1);
            i++;
        }
    }
    write(1, "\n", 1);
}